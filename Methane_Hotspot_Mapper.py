"""# 10. Data Quality Preparation

Before performing statistical analysis, the Sentinel-5P methane observations are prepared by selecting the methane band and clipping all outputs to the Damodar Valley study area. This ensures that subsequent calculations are restricted to the region of interest and reduces unnecessary processing.
"""

# ==========================================================
# Select methane band
# ==========================================================

CH4_BAND = "CH4_column_volume_mixing_ratio_dry_air"

ch4_collection = CH4_COLLECTION.select(CH4_BAND)

print("Methane band selected successfully.")

