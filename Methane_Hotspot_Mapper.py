"""# 11. Historical Baseline Dataset

A historical baseline is generated using observations between **2019 and 2021**.

This baseline represents the normal methane conditions in the Damodar Valley Coal Belt and will be used to identify statistically significant methane anomalies during the analysis period.
"""

# ==========================================================
# Historical Baseline
# ==========================================================

baseline = (
    ch4_collection
    .filterDate(BASELINE_START, BASELINE_END)
)

print("Baseline Images :", baseline.size().getInfo())

"""# 12. Baseline Statistics

Three statistical layers are generated from the baseline dataset:

- Mean methane concentration
- Median methane concentration
- Standard deviation

These layers form the reference against which monthly methane observations are compared.
"""

# ==========================================================
# Baseline Mean
# ==========================================================

baseline_mean = baseline.mean().clip(aoi)

# ==========================================================
# Baseline Median
# ==========================================================

baseline_median = baseline.median().clip(aoi)

# ==========================================================
# Baseline Standard Deviation
# ==========================================================

baseline_std = (
    baseline
    .reduce(ee.Reducer.stdDev())
    .clip(aoi)
)

print("Baseline statistics created successfully.")

