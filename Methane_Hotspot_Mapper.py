"""# 34. Calculate Hotspot Area

Each persistent hotspot polygon is assigned an area in square kilometres.

This provides a quantitative estimate of the spatial extent of each methane hotspot.
"""

def add_area(feature):

    area = (
        feature.geometry()
        .area(
            ee.ErrorMargin(1)
        )
        .divide(1e6)
    )

    return feature.set(
        "Area_km2",
        area
    )

hotspots_area = vectors.map(add_area)

print("Area Calculated Successfully")

"""# 35. Calculate Hotspot Centroid Coordinates

Latitude and longitude coordinates are calculated for each hotspot centroid.
"""

def add_coordinates(feature):

    centroid = feature.geometry().centroid(
        ee.ErrorMargin(1)
    )

    coordinates = centroid.coordinates()

    return (
        feature
        .set(
            "Longitude",
            coordinates.get(0)
        )
        .set(
            "Latitude",
            coordinates.get(1)
        )
    )

hotspots_coordinates = hotspots_area.map(add_coordinates)

print("Coordinates Added Successfully")

"""# 36. Rank Persistent Hotspots

Hotspots are ranked according to their spatial extent.

Larger hotspot polygons are assigned higher priority for further investigation.
"""

# ==========================================================
# Sort Hotspots by Area
# ==========================================================

def add_hotspot_score(feature):

    area = ee.Number(
        feature.get("Area_km2")
    )

    score = area

    return feature.set(
        "Hotspot_Score",
        score
    )

ranked_hotspots = (
    hotspots_coordinates
    .map(add_hotspot_score)
    .sort(
        "Hotspot_Score",
        False
    )
)

print("Hotspots Ranked Successfully")

